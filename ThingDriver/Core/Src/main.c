void printFloat(float v, int decimalDigits, char s[])
{
  int i = 1;
  int intPart, fractPart;
  for (; decimalDigits != 0; i *= 10, decimalDigits--)
    ;
  intPart = (int)v;
  fractPart = (int)((v - (float)(int)v) * i);
  if (fractPart < 0)
    fractPart *= -1;
  sprintf(s, "%i.%i", intPart, fractPart);
}
